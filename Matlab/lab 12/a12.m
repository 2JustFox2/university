%% задание правой части уравнения
% исходное уравнение:
% y^2 dx - (x*y + x^3)dy = 0
% приводим к виду y' = f(x,y)
% dy/dx = y^2 / (x*y + x^3)

f = @(x,y) y.^2 ./ (x .* y + x.^3);

%% задание начальных условий и интервала
x0 = 1;
y0 = 1;
xn = 2;

%% аналитическое решение с помощью dsolve
% задаём символьные переменные
syms y(x)

% записываем дифференциальное уравнение
ode = diff(y,x) == y^2 / (x*y + x^3);

% задаём начальное условие
cond = y(1) == 1;

% получаем аналитическое решение
ys = dsolve(ode,cond);

% выводим аналитическое решение
fprintf('аналитическое решение:\n');
disp(ys)

%% преобразование аналитического решения в функцию
% это нужно для вычисления точных значений
y_exact = matlabFunction(ys);

%% метод эйлера с шагом 0.2
% задаём шаг интегрирования
h1 = 0.2;

% формируем сетку
x_euler_02 = x0:h1:xn;

% определяем число узлов
n1 = length(x_euler_02);

% создаём массив решения
y_euler_02 = zeros(1,n1);

% записываем начальное условие
y_euler_02(1) = y0;

% цикл метода эйлера
for i = 1:n1-1
    % вычисляем следующее значение функции
    y_euler_02(i+1) = y_euler_02(i) + h1 * f(x_euler_02(i),y_euler_02(i));
end

%% метод эйлера с шагом 0.05
% задаём шаг интегрирования
h2 = 0.05;

% формируем сетку
x_euler_005 = x0:h2:xn;

% определяем число узлов
n2 = length(x_euler_005);

% создаём массив решения
y_euler_005 = zeros(1,n2);

% записываем начальное условие
y_euler_005(1) = y0;

% цикл метода эйлера
for i = 1:n2-1
    % вычисляем следующее значение функции
    y_euler_005(i+1) = y_euler_005(i) + h2 * f(x_euler_005(i),y_euler_005(i));
end

%% метод рунге-кутта 4 порядка с шагом 0.2
% формируем сетку
x_rk4_02 = x0:h1:xn;

% определяем число узлов
n3 = length(x_rk4_02);

% создаём массив решения
y_rk4_02 = zeros(1,n3);

% записываем начальное условие
y_rk4_02(1) = y0;

% цикл метода рунге-кутта
for i = 1:n3-1

    % вычисляем коэффициент k1
    k1 = f(x_rk4_02(i),y_rk4_02(i));

    % вычисляем коэффициент k2
    k2 = f(x_rk4_02(i) + h1/2 , y_rk4_02(i) + h1*k1/2);

    % вычисляем коэффициент k3
    k3 = f(x_rk4_02(i) + h1/2 , y_rk4_02(i) + h1*k2/2);

    % вычисляем коэффициент k4
    k4 = f(x_rk4_02(i) + h1 , y_rk4_02(i) + h1*k3);

    % вычисляем следующее значение функции
    y_rk4_02(i+1) = y_rk4_02(i) + h1*(k1 + 2*k2 + 2*k3 + k4)/6;
end

%% метод рунге-кутта 4 порядка с шагом 0.05
% формируем сетку
x_rk4_005 = x0:h2:xn;

% определяем число узлов
n4 = length(x_rk4_005);

% создаём массив решения
y_rk4_005 = zeros(1,n4);

% записываем начальное условие
y_rk4_005(1) = y0;

% цикл метода рунге-кутта
for i = 1:n4-1

    % вычисляем коэффициент k1
    k1 = f(x_rk4_005(i),y_rk4_005(i));

    % вычисляем коэффициент k2
    k2 = f(x_rk4_005(i) + h2/2 , y_rk4_005(i) + h2*k1/2);

    % вычисляем коэффициент k3
    k3 = f(x_rk4_005(i) + h2/2 , y_rk4_005(i) + h2*k2/2);

    % вычисляем коэффициент k4
    k4 = f(x_rk4_005(i) + h2 , y_rk4_005(i) + h2*k3);

    % вычисляем следующее значение функции
    y_rk4_005(i+1) = y_rk4_005(i) + h2*(k1 + 2*k2 + 2*k3 + k4)/6;
end

%% вычисление точного решения
% вычисляем точное решение для сетки 0.2
y_exact_02 = y_exact(x_rk4_02);

% вычисляем точное решение для сетки 0.05
y_exact_005 = y_exact(x_rk4_005);

%% вычисление абсолютной погрешности
% абсолютная погрешность метода эйлера при шаге 0.2
err_euler_02 = abs(y_exact_02 - y_euler_02);

% абсолютная погрешность метода эйлера при шаге 0.05
err_euler_005 = abs(y_exact_005 - y_euler_005);

% абсолютная погрешность метода рунге-кутта при шаге 0.2
err_rk4_02 = abs(y_exact_02 - y_rk4_02);

% абсолютная погрешность метода рунге-кутта при шаге 0.05
err_rk4_005 = abs(y_exact_005 - y_rk4_005);

%% оценка погрешности по рунге для метода эйлера
% порядок метода эйлера равен 1
p1 = 1;

% выбираем общие точки сетки
coarse_euler = y_euler_02;
fine_euler = y_euler_005(1:4:end); % каждый 4й элемент

% вычисляем оценку по рунге
runge_euler = abs(fine_euler - coarse_euler) / (2^p1 - 1);

%% оценка погрешности по рунге для метода рунге-кутта
% порядок метода рунге-кутта равен 4
p2 = 4;

% выбираем общие точки сетки
coarse_rk4 = y_rk4_02;
fine_rk4 = y_rk4_005(1:4:end);

% вычисляем оценку по рунге
runge_rk4 = abs(fine_rk4 - coarse_rk4) / (2^p2 - 1);

%% вывод абсолютной погрешности в конце интервала
fprintf('\n');
fprintf('абсолютная погрешность в конце интервала:\n');

fprintf('метод эйлера h=0.2 : %e\n',err_euler_02(end));
fprintf('метод эйлера h=0.05 : %e\n',err_euler_005(end));

fprintf('метод рунге-кутта 4 порядка h=0.2 : %e\n',err_rk4_02(end));
fprintf('метод рунге-кутта 4 порядка h=0.05 : %e\n',err_rk4_005(end));

%% вывод оценки погрешности по рунге
fprintf('\n');
fprintf('оценка погрешности по рунге:\n');

fprintf('метод эйлера:\n');
disp(runge_euler)

fprintf('метод рунге-кутта 4 порядка:\n');
disp(runge_rk4)

