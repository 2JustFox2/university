%% Проверка является функция решением

syms y(x) C1
%методом dsolve
sol = dsolve(x^3*(diff(y,x)-x) == y^2);
y1 = x^2/log(x)*(1-log(x)); %%ессли logx -1 то подходит иначе нет
y2 = x^2;
disp(sol);
